// Cinco Programas

function adicao(a,b){
    return a+b;
}


function subtracao(a,b){
    return a-b;
}

function multiplicacao(a,b){
    return a*b;
}

function divisao(a,b){
    return a/b;
}

function modulo(a,b){
    return a%b;
}

console.log(adicao(20,5));
console.log(subtracao(20,5));
console.log(multiplicacao(20,5));
console.log(divisao(20,5));
console.log(modulo(20,5));